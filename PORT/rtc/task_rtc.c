/**
 ******************************************************************************
 * @file    task_rtc.c
 * ============================================================
 * @brief   任务7:实时时间刷新任务
 * 
 * 功能说明:
 * - 每秒从RTC硬件时钟读取时间
 * - 更新全局时间变量
 * - 通知屏幕任务刷新时间显示
 * - 管理员修改时间时立即刷新
 * ============================================================
 */
#include "rtc.h"
#include "freertos.h"
#include "app_global.h"
#include "app_define.h"
#include "cmsis_os.h"
#include "main.h"

/* ============================================
 * 常量定义
 * ============================================ */
#define RTC_UPDATE_INTERVAL     1000        // RTC更新时间间隔(ms)

/* ============================================
 * 时间格式化缓冲区
 * ============================================ */
static char g_TimeBuffer[16];

/* ============================================
 * StartRTCTask - 实时时间刷新任务
 * ============================================ */
void StartRTCTask(void *argument) {
    
    (void)argument;  // 防止编译警告
    
    uint32_t lastUpdateTime = 0;
    
    /* ============================================
     * 主循环
     * ============================================ */
    while(1) {
        
        /* ============================================
         * 检查是否到了更新时间
         * ============================================ */
        uint32_t currentTime = osGetTickCount();
        
        if((currentTime - lastUpdateTime) >= RTC_UPDATE_INTERVAL) {
            
            /* ============================================
             * 获取RTC时间和日期
             * ============================================ */
            HAL_RTC_GetTime(&hrtc, &g_CurrentTime, RTC_FORMAT_BIN);
            HAL_RTC_GetDate(&hrtc, &g_CurrentDate, RTC_FORMAT_BIN);
            
            /* ============================================
             * 格式化时间字符串
             * ============================================ */
            //snprintf(g_TimeStr, sizeof(g_TimeStr), "%02d:%02d:%02d", 
                    // g_CurrentTime.Hours, g_CurrentTime.Minutes, g_CurrentTime.Seconds);
            
            /* ============================================
             * 设置显示更新标志
             * ============================================ */
            g_DisplayUpdate = 1;
            
            /* ============================================
             * 更新时间戳
             * ============================================ */
            lastUpdateTime = currentTime;
        }
        
        /* ============================================
         * 短暂延时
         * ============================================ */
        osDelay(100);
    }
}
